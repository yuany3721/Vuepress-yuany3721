<h2 id="美军网络战历史" tabindex="-1"><a class="header-anchor" href="#美军网络战历史"><span>美军网络战历史</span></a></h2>
<p>美国是世界上第一个提出网络战概念的国家。按照美军的定义，所谓“网络战”，即为攻击、干扰和破坏敌方网络信息系统，并保证己方网络信息系统正常运行而采取的系列网络攻防行动。 事实上，早在互联网普及前的 1993 年，美国著名智库兰德公司的研究人员就提出了网络战即将到来的理论，并对网络战的概念及其作战理念进行了前瞻性探讨。随着网络技术和网络攻防手段的不断发展，许多政府部门、商业组织和社会团体利用网络来制造、影响和控制舆论。特别是美国，近年来不断利用网络插手别国事务，网络战也从单纯的攻防战转向舆论战与宣传战，从“无政府”走向“有组织”，从隐蔽走向公开。</p>
