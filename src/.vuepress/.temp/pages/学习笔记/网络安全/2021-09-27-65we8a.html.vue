</ul>
<h2 id="网络战的特点" tabindex="-1"><a class="header-anchor" href="#网络战的特点"><span>网络战的特点</span></a></h2>
<ul>
<li>与其他作战形式相比，网络战的灵活性十分明显：
<ul>
<li>既可以是有组织的战略、战役行动，也可以是无组织的个人行为</li>
<li>既可能发生在战略对手之间，也可能殃及他国甚至盟国</li>
<li>既深刻威胁国家的战争体系，也广泛威胁国家的政治、经济、金融等各个领域</li>
</ul>
</li>
<li>从本质上说，网络战仍是传统战争策略的延伸：
<ul>
<li>界限模糊 网络战中，战略性、战役性和战术性信息在集成化网络环境中有序流动，呈现出紧密互联、相互融合的特点。这使得网络战的战略、战役、战术界限模糊，甚至融为一体。另一方面，是战场不定。传统战争离不开陆地、海洋、空中和太空等有形空间，而网络战是在无形的网络空间进行。网络战场消除了地理空间的界限，使得前方、后方、前沿、纵深的传统作战概念变得模糊，攻防界限很难划分。</li>
<li>手段相差异 网络战的手段大体可分为以下几种：网络间谍、网络破坏、政治宣传、收集数据、系统攻击、中断信息设备、攻击基础设施、隐藏恶意软件等等。但无论什么方式，获取信息控制权是网络战的制胜关键。</li>
</ul>
</li>
</ul>
