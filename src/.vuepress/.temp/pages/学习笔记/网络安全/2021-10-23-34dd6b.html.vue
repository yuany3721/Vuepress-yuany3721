<p>在网络协议栈中的过滤层次</p>
<ul>
<li>包过滤防火墙（分组过滤防火墙）：根据网络层的信息进行控制。根据企业的安全策略定义一组访问控制规则，然后防火墙在内存中建立一张与访问控制规则对应的访问控制列表。对于每个数据包，如果在访问控制列表中有对应的项，则防火墙按规则的要求允许或拒绝数据包的通过，否则应用默认规则（丢弃或允许）
<ul>
<li>静态包过滤防火墙：访问控制列表在运行过程中不会动态变化，其过滤规则只利用了 IP 与 TCP/UDP 报头中的几个字段，只适合一些对安全要求不高的场合，其访问控制规则的配置比较复杂，对于某些需要打开动态端口的应用，很难定义合适的规则</li>
<li>动态包过滤防火墙：通过检查应用程序信息以及连接信息，来判断某个端口是否需要临时打开，当传输结束时，端口又可以恢复为关闭状态。只有在主机主动地跟外界连接时，其他的机器才可以与它连接</li>
</ul>
