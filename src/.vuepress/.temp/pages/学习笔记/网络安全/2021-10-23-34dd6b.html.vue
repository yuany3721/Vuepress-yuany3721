<p>堡垒主机：是一种配置了较为全面的安全防范措施的网络上的计算机，它为网络间的通信提供了一个阻塞点。通常堡垒主机可以用作应用级和电路级网关的平台，是一个组织机构网络安全的中心主机 特征：</p>
<ol>
<li>堡垒主机硬件平台运行较为安全的操作系统，成为可信任的系统</li>
