<p>防火墙的性能指标</p>
<ul>
<li>吞吐量：通常用防火墙在不丢包的条件下每秒转发包的最大数目来表示</li>
<li>时延：在防火墙最大吞吐量的情况下，数据包从到达防火墙到被防火墙转发出去的时间间隔</li>
<li>丢包率：在不同负载的情况下，因为来不及处理而不得不丢弃的数据包占收到的数据包总数的比例。不同的负载量通常在最小值到防火墙的线速值（防火墙的最高数据包转发速率）之间变化，一般选择线速的 10%作为负载增量的步长</li>
<li>背对背：从空闲状态开始，以达到传输介质最小合法间隔极限的传输速率发送相当数量的固定长度的帧，当出现第一个帧丢失时，发送的帧数。描述了网络设备承受突发数据的能力</li>
<li>最大位转发率：位转发率指在特定负载下每秒钟防火墙将允许的数据流转发至正确的目的接口的位数。最大位转发率指在不同的负载下反复测量得出的位转发率数值中的最大值</li>
<li>最大并发连接数：指穿越防火墙的主机之间或主机与防火墙之间能同时建立的最大连接数。这项性能可以反映一定流量下防火墙所能顺利建立和保持的并发连接数及一定数量的连接情况下防火墙的吞吐量变化</li>
<li>最大并发连接建立速率：每秒所能建立起的 TCP/HTTP 连接数及防火墙所能保持的最大 TCP/HTTP 连接数</li>
<li>有效通过率：总转发数据量(bit/s)减去丢失的和重发的数据量(bit/s)</li>
<li>其他性能指标：最大策略书、平均无故障间隔时间等</li>
</ul>
